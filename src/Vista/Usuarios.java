    private void btnBuscarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarImagenActionPerformed
        if (seleccionado.showDialog(null, "ABRIR ARCHIVO") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionado.getSelectedFile();
            if (archivo.canRead()) {
                if (archivo.getName().endsWith(".png") || archivo.getName().endsWith(".jpg") || archivo.getName().endsWith(".gif") || archivo.getName().endsWith(".jpeg")) {
                    try {
                        String p = archivo.getPath();
                        lblPath.setText(p);
                        ImageIcon imagen = new ImageIcon(p);
                        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_DEFAULT));
                        lblImagen.setIcon(icono);
                        lblImagen.repaint();
//                        bytesImg = new UsuariosControl().AbrirImagen(archivo);
//                        lblImagen.setIcon(new ImageIcon(bytesImg));
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "SELECCIONE UN TIPO DE ARCHIVO VALIDO: png, jpg, jpeg, gif");
                }
            }
        }
    }//GEN-LAST:event_btnBuscarImagenActionPerformed

