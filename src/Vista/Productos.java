    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        try {
            int fila = tblProductos.getSelectedRow();
            int id = Integer.parseInt(tblProductos.getValueAt(fila, 0).toString());
            //int idCategoria = Integer.parseInt(tblProductos.getValueAt(fila, 4).toString());
            Object[] datos = (Object[]) new ProductosControl().capturarCamposDeTabla(id);
            txtCodigo.setText("" + datos[0]);
            txtNombre.setText("" + datos[1]);
            //new ProductosControl().comboCategoriaSeleccionada(cmbCategoria, idCategoria);
            btnEliminar.setEnabled(true);
            btnActualizar.setEnabled(true);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_tblProductosMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String nombre = txtNombre.getText().toUpperCase();
            //int categoria = Integer.parseInt(txtCategoria.getText());
            //int presentacion = Integer.parseInt(txtPresentacion.getText());
            Producto p = new Producto();
            p.setNombre(nombre);
            //Corregir con los ComboBox
            //p.setIdCategoria(new ProductosControl().getIdCategoriaConNombre(cmbCategoria.getSelectedItem().toString()));            

            if (new ProductoDAO().registrar(p)) {
                JOptionPane.showMessageDialog(rootPane, "SE REGISTRÓ EL PRODUCTO");
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERROR DE REGISTRO");
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            int fila = tblProductos.getSelectedRow();
            int idProducto = Integer.parseInt(txtCodigo.getText());
            String nombre = txtNombre.getText();
            //int categoria = new ProductosControl().getIdCategoriaConNombre(cmbCategoria.getSelectedItem().toString());            
            Producto p = new Producto();
            p.setNombre(nombre);
            //Corregir con los comboBox
            //p.setIdCategoria(categoria);
            p.setIdProducto(idProducto);
            if (new ProductoDAO().modificar(p)) {
                JOptionPane.showMessageDialog(rootPane, "SE MODIFICÓ EL PRODUCTO");
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERROR DE MODIFICACION");
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

