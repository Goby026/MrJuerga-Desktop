    public void totales() {
        double ingresos = 0.0;
        double egresos = 0.0;
        double igv = 0.0;
        int numFilas = tblResumen.getRowCount();
        for (int i = 0; i < numFilas; i++) {
            ingresos += Double.parseDouble(tblResumen.getValueAt(i, 4).toString());
            egresos += Double.parseDouble(tblResumen.getValueAt(i, 5).toString());
            igv += Double.parseDouble(tblResumen.getValueAt(i, 6).toString());
        }
        txtIngresos.setText(""+ new Validaciones().Redondear(ingresos));
        txtEgresos.setText(""+ new Validaciones().Redondear(egresos));
        txtIgv.setText(""+ new Validaciones().Redondear(igv));
    }

