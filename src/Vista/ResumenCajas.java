    public void resumenCajas(int mes, int caja) throws Exception {
        limpiarTablaSalidas();
        Object datos[] = new Object[7];
        Conexion c = new Conexion();
        c.conectar();
        Connection con = c.getConexion();
        try {
            PreparedStatement query = con.prepareStatement("select distinct flujocaja.idflujocaja, usuario.nombre, flujocaja.fecha_inicio, flujocaja.fecha_final, round(flujocaja.ingresos,2) as ingresos,round(flujocaja.egresos,2) as egresos, round(((flujocaja.ingresos/1.18)*0.18),2) as IGV\n"
                    + "	from venta\n"
                    + "	inner join flujocaja on venta.idflujocaja = flujocaja.idflujocaja\n"
                    + "	inner join usuario on venta.idusuario = usuario.idusuario\n"
                    + "	inner join caja on flujocaja.idcaja = caja.idcaja\n"
                    + "	where month(flujocaja.fecha) = ? and flujocaja.estado = 0 and flujocaja.idcaja = ?");
            query.setInt(1, mes);
            query.setInt(2, caja);
            ResultSet rs = query.executeQuery();
            while (rs.next()) {
                datos[0] = rs.getInt("flujocaja.idflujocaja");
                datos[1] = rs.getString("usuario.nombre");
                datos[2] = rs.getString("flujocaja.fecha_inicio");
                datos[3] = rs.getString("flujocaja.fecha_final");
                datos[4] = rs.getString("ingresos");
                datos[5] = rs.getString("egresos");
                datos[6] = rs.getString("IGV");
                modelo.addRow(datos);
            }
            tblResumen.setModel(modelo);
            //sumaDeSalidas();
            query.close();
            rs.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally {
            c.cerrar();
        }
    }

    public void limpiarTablaSalidas() {
        for (int i = 0; i < tblResumen.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

