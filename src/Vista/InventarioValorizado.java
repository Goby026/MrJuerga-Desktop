    public double calcularValorizacionTotal() {
        double monto = 0;
        for (int i = 0; i < tblInventario.getRowCount(); i++) {
            monto += Double.parseDouble(tblInventario.getValueAt(i, 6).toString());
        }
        return monto;
    }

