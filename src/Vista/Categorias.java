    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (new Validaciones().validarCampoVacio(txtDescripcion)) {
            if (new Validaciones().validarCampoVacio(txtCodCategoria)) {
                String Descripcion = txtDescripcion.getText().toUpperCase();
                String CodCategoria = txtCodCategoria.getText().toUpperCase();
                try {
                    Categoria ca = new Categoria();
                    ca.setDescripcion(Descripcion);
                    ca.setCodcategoria(CodCategoria);
                    CategoriaDAO dc = new CategoriaDAO();
                    if (dc.Registrar(ca)) {
                        JOptionPane.showMessageDialog(rootPane, "DESCRIPCION REGISTRADA");
                        CategoriaControl cc = new CategoriaControl();
                        cc.RefrescarTablaCategoria(tblCategorias);
                        txtDescripcion.setText("");
                        txtCodCategoria.setText("");
                    }
                } catch (Exception e) {
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "INGRESE CODIGO CATEGORIA");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "INGRESE DESCRIPCION ");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (new Validaciones().validarCampoVacio(txtDescripcion)) {
            if (new Validaciones().validarCampoVacio(txtCodCategoria)) {
                Categoria c = new Categoria();
                int fila = tblCategorias.getSelectedRow();
                if (fila < 0) {
                    JOptionPane.showMessageDialog(rootPane, "SELECCIONE UNA CATEGORIA");
                } else {
                    int id = Integer.parseInt(tblCategorias.getValueAt(fila, 0).toString());
                    c.setIdcategoria(id);
                    c.setDescripcion(txtDescripcion.getText().toUpperCase());
                    c.setCodcategoria(txtCodCategoria.getText().toUpperCase());
                    CategoriaDAO dc = new CategoriaDAO();
                    try {
                        if (dc.Modificar(c)) {
                            new CategoriaControl().RefrescarTablaCategoria(tblCategorias);
                            JOptionPane.showMessageDialog(rootPane, "SE ACTUALIZO EL REGISTRO");
                            txtDescripcion.setText("");
                            txtCodCategoria.setText("");
                            btnRegistrar.setEnabled(false);
                            btnEliminar.setEnabled(false);
                            btnModificar.setEnabled(false);
                        }

                    } catch (Exception ex) {
                        Logger.getLogger(Categorias.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "INGRESE CODIGO CATEGORIA");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "INGRESE DESCRIPCION ");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblCategorias.getSelectedRow();
        if (new Validaciones().validarCampoVacio(txtDescripcion)) {
            if (fila >= 0) {
                CategoriaDAO da = new CategoriaDAO();
                Categoria a = new Categoria();
                int id = Integer.parseInt(tblCategorias.getValueAt(fila, 0).toString());
                a.setIdcategoria(id);
                try {
                    da.Eliminar(a);
                    new CategoriaControl().RefrescarTablaCategoria(tblCategorias);
                    JOptionPane.showMessageDialog(rootPane, "CATEGORIA ELIMINADA");
                    txtDescripcion.setText("");
                    txtCodCategoria.setText("");
                } catch (Exception ex) {
                    System.out.println(ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "SELECIONE UNA CATEGORIA");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

