    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            //obtener el id de la caja seleccionada
            int filaSelec = tblCajas.getSelectedRow();
            if (filaSelec >= 0) {
                if (new Validaciones().validarCampoVacio(txtNomCaja)) {
                    int idCaja = Integer.parseInt(tblCajas.getValueAt(filaSelec, 0).toString());
                    String nombre = txtNomCaja.getText().toUpperCase();
                    Caja c = new Caja();
                    c.setNomCaja(nombre);
                    c.setIdCaja(idCaja);
                    CajaDAO cdao = new CajaDAO();
                    if (cdao.Modificar(c)) {
                        JOptionPane.showMessageDialog(rootPane, "SE MODIFICO CORRECTAMENTE");
                        new CajaControl().RefrescarTablaCajas(tblCajas);
                    } else {
                        System.out.println("ERROR");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "INDIQUE NUEVO NOMBRE DE CAJA");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "SELECCIONE UNA FILA DE LA TABLA CAJA");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int flag=0;
            int numFilas = tblPersonalAsignado.getRowCount();
            for (int i = 0; i < numFilas; i++) {
                String usuario = tblPersonalAsignado.getValueAt(i, 0).toString();
                String caja = tblPersonalAsignado.getValueAt(i, 1).toString();
                if (new CajaControl().registrarUsuarioCaja(usuario, caja)) {
                    flag++;
                }
            }
            if (flag>0) {
                JOptionPane.showMessageDialog(rootPane, "SE ASIGNO CORRECTAMENTE AL USUARIO");
            }
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

