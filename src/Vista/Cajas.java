    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (new Validaciones().validarCampoVacio(txtNomCaja)) {
                String nomCaja = txtNomCaja.getText().toUpperCase();
                //System.out.println(nomCaja);
                JOptionPane.showMessageDialog(rootPane, new CajaControl().guardarCaja(nomCaja, tblCajas));
                txtNomCaja.setText("");
                cmbCajas.removeAllItems();
                new CajaControl().cargarComboCajas(cmbCajas);
            } else {
                JOptionPane.showMessageDialog(rootPane, "INDIQUE UN NOMBRE DE CAJA");
                txtNomCaja.requestFocus();
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        int filaSelec = tblUsuarios.getSelectedRow();
        if (filaSelec >= 0) {
            String[] datos = new String[2];
            datos[0] = tblUsuarios.getValueAt(filaSelec, 1).toString();
            datos[1] = cmbCajas.getSelectedItem().toString();
            modeloUsuarioCaja.addRow(datos);
            tblPersonalAsignado.setModel(modeloUsuarioCaja);
        } else {
            JOptionPane.showMessageDialog(rootPane, "SELECCIONE UN USUARIO");
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

