    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            //validar que la tabla tenga filas agregadas
            if (tblProductoPresentacion.getRowCount() >= 0) {
                //metodo para guardar los datos a la bd
                int numFilas = tblProductoPresentacion.getRowCount();
                for (int i = 0; i < numFilas; i++) {
                    String producto = tblProductoPresentacion.getValueAt(i, 0).toString();
                    String categoria = tblProductoPresentacion.getValueAt(i, 1).toString();
                    String presentacion = tblProductoPresentacion.getValueAt(i, 2).toString();
                    String almacen = tblProductoPresentacion.getValueAt(i, 3).toString();
                    int stock = Integer.parseInt(tblProductoPresentacion.getValueAt(i, 4).toString());
                    double precio = Double.parseDouble(tblProductoPresentacion.getValueAt(i, 5).toString());
                    if (new ProductoPresentacionControl().guardarGestionProducto(producto, categoria, presentacion, almacen, stock, precio) > 0) {
                        System.out.println("OK");
                    } else {
                        System.out.println("ERROR");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(getRootPane(), "AGREGUE ELEMENTOS A LA TABLA DE GESTION DE PRODUCTOS");
            }
            //metodo para limpiar si todo se opero correctamente
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

